    public void showEmpDate() {
        try {
//            getAllEmp() returns a reference(type of list) that's why we made list type reference here also
            List<EmployeePojo> emplist = EmployeeDao.getAllEmp();
//        JTable is an object no method is availible for inserting data or retrieving data , so for doing this we need table model , and for getting table moddel we call getModel()(of JTable class) , which returns a model object and we store this model in a variable/reference and whatever the changes we make on this variable , will actuallu affect on the table
            DefaultTableModel tm = (DefaultTableModel) jtAllEmp.getModel();
//        addRow() of table takes an array of Object 
            Object rows[] = new Object[4];
            for (EmployeePojo emp : emplist) {
                rows[0] = emp.getEmpId();
                rows[1] = emp.getEmpName();
                rows[2] = emp.getJob();
                rows[3] = emp.getSalary();
                tm.addRow(rows);
            }
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(null, "Error in View all employees!", "Error", JOptionPane.ERROR_MESSAGE);
            sq.printStackTrace();
            return;
        }
    }

