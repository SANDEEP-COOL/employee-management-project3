    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int row = jtProducts.getSelectedRow();
        if( row == -1 ){
            JOptionPane.showMessageDialog(null, "No row is selected!", "Information", JOptionPane.INFORMATION_MESSAGE);
//            btnDelete.setEnabled(false);
            return;
        }
        btnDelete.setEnabled(true);
        String id = tm.getValueAt(row,0).toString();
//        System.out.println(id);
        try{
            if ( ProductDao.deleteProduct(id) ) {
                JOptionPane.showMessageDialog(null, "Product is deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
//                for keeping the table empty
                tm.setRowCount(0);
//                refreshing the table
                loadProductDetails();
                return;
            }
            else{
                JOptionPane.showMessageDialog(null, "Product is not deleted!", "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        }
        catch(SQLException sq){
            JOptionPane.showMessageDialog(null, "Error in DB while Deleting the Product!", "Error", JOptionPane.ERROR_MESSAGE);
            sq.printStackTrace();
            return;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

