    private void loadProductDetails() {
        try {
            List<ProductsPojo> plist = ProductDao.getAllProductsDetails();
            if ( plist.isEmpty() ) {
                JOptionPane.showMessageDialog(null, "No Product is Availible , can not delete!", "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            btnDelete.setEnabled(true);
//           create a model , so that we can use methods to add items on the table
            tm = (DefaultTableModel) jtProducts.getModel();
//            creating an array of Object class
            Object row[] = new Object[7];
            for ( ProductsPojo p: plist ) {
                row[0] = p.getPid();
                row[1] = p.getPname();
                row[2] = p.getPcompany();
                row[3] = p.getPprice();
                row[4] = p.getOurprice();
                row[5] = p.getQuantity();
                row[6] = p.getPtax();
//                calling addRow() of table
                tm.addRow(row);
            }
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(null, "Error in DB while deleting product!", "Error", JOptionPane.ERROR_MESSAGE);
            sq.printStackTrace();
            return; 
        }
    }
