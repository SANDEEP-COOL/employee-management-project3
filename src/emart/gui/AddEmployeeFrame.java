    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        //check whether all the details are filled or not , if validateEmpInput gives us false
        if (!validateInputs()) {
            JOptionPane.showMessageDialog(null, "Please fill all the details properly!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //extracting empname and salary from addEmployeeFrame , science salary is type of int that's why we convert it into it's actual datatype which is int
        String empName = txtName.getText();
        int salary = Integer.parseInt(txtSalary.getText());
        String empId = txtId.getText();
        String job = cmbJob.getSelectedItem().toString();

        try {
        //now adding these details in employeePojo object
        EmployeePojo empPojo = new EmployeePojo();
        empPojo.setEmpId(empId);
        empPojo.setEmpName(empName);
        empPojo.setJob(job);
        empPojo.setSalary(salary);
//        adding this employee in the employeestable
        boolean x = EmployeeDao.addEmployee(empPojo);
            if(!x){
                JOptionPane.showMessageDialog(null, "employee not added", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
           JOptionPane.showMessageDialog(null, "employee added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
//           after successfully addition of employee , we have to clear empName and empSal , and for that we use a method called clearText()
            clearText();
            //again we need to load the next id overhere
            loadEmpId();
           
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(null, "Error in adding new employee!", "Error", JOptionPane.ERROR_MESSAGE);
            sq.printStackTrace();
            return;
        }
        catch(NumberFormatException nfm){
            JOptionPane.showMessageDialog(null, "Please enter numeric data for salary!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnAddActionPerformed

