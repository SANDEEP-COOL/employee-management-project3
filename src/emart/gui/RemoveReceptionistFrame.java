    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        try {
            String userid = jcID.getSelectedItem().toString();
            boolean ans = ReceptionistDao.deleteReceptionist(userid);
            if (ans) {
                 JOptionPane.showMessageDialog(null, "Receptionist is deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                 jcID.removeItemAt(jcID.getSelectedIndex());
                 if ( jcID.getItemCount() == 0 ) {
                     btnDelete.setEnabled(false);
                     
                 }
                 return;
            }
            JOptionPane.showMessageDialog(null, "Receptionist is not deleted", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(null, "Error in DB while deleting receptionist!", "Error", JOptionPane.ERROR_MESSAGE);
            sq.printStackTrace();
            return;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

